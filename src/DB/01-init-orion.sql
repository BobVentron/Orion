CREATE TABLE interfaces_poe (
    interface_id     BIGINT PRIMARY KEY,
    admin_status     BOOLEAN DEFAULT FALSE,
    detection_status VARCHAR(50),
    power_class      INT,

    CONSTRAINT fk_poe_interface FOREIGN KEY (interface_id) REFERENCES interfaces(id) ON DELETE CASCADE,
    CONSTRAINT chk_poe_class    CHECK (power_class BETWEEN 0 AND 4)
