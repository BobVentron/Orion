CREATE TABLE scan_profiles (
    id                  INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name                VARCHAR(100) NOT NULL,
    type                VARCHAR(50)  NOT NULL,

    -- Fréquence (uniquement pour SNMP_Discovery sur scan_networks)
    -- Les autres types sont gérés via monitoring_profiles
    interval_seconds    INT NOT NULL DEFAULT 3600,

    timeout_ms          INT DEFAULT 2000,
    retry_count         INT DEFAULT 1,
    concurrency_threads INT DEFAULT 10,
    packet_delay_ms     INT DEFAULT 0,
    is_enabled          BOOLEAN DEFAULT TRUE,
    created_at          TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at          TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

    CONSTRAINT chk_scan_type CHECK (type IN (
        'SNMP_Discovery',   -- Découverte complète d'un subnet
        'ICMP_Ping',        -- Ping subnet (usage limité — préférer monitoring_profiles)
        'Port_Scan',        -- Scan de ports TCP
        'SNMP_Metrics',     -- Usage limité — préférer monitoring_profiles
        'SNMP_Full'         -- Usage limité — préférer monitoring_profiles
    )),
    CONSTRAINT chk_scan_params CHECK (timeout_ms > 0 AND retry_count >= 0 AND concurrency_threads > 0),
    CONSTRAINT chk_interval    CHECK (interval_seconds >= 0)
);

