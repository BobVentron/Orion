    id                  INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subnet              CIDR    NOT NULL,
    exclude_ips         JSONB   DEFAULT '[]'::jsonb,
    description         VARCHAR(255),
    scan_profile_id     INT     NOT NULL,
    auth_profile_id     INT     NOT NULL,
    next_scan_at        TIMESTAMP WITH TIME ZONE,
    interval_seconds    INT     NOT NULL DEFAULT 86400,  -- 24h par défaut pour les découvertes
    last_scan_status    VARCHAR(20) DEFAULT 'idle',
    last_scan_at        TIMESTAMP WITH TIME ZONE,
    last_scan_duration  INT,
    last_hosts_found    INT DEFAULT 0,
    last_error          TEXT,
    created_at          TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at          TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

    CONSTRAINT fk_scan_net_scan_profile FOREIGN KEY (scan_profile_id) REFERENCES scan_profiles(id)  ON DELETE RESTRICT,
    CONSTRAINT fk_scan_net_auth_profile FOREIGN KEY (auth_profile_id) REFERENCES auth_profiles(id) ON DELETE RESTRICT,
    CONSTRAINT chk_scan_net_status      CHECK (last_scan_status IN ('idle', 'running', 'completed', 'failed')),

    -- Unicité : même subnet peut être configuré avec des profils auth différents
    --          ou des profils de scan différents
    CONSTRAINT uq_scan_network_subnet_profile UNIQUE (subnet, auth_profile_id, scan_profile_id)
);

