CREATE TABLE vlans (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    device_id       UUID        NOT NULL,
    vlan_tag        INT         NOT NULL,
    name            VARCHAR(100),
    status          VARCHAR(20) DEFAULT 'Active',
    type            VARCHAR(20) DEFAULT 'Ethernet',
    role            VARCHAR(20) DEFAULT 'Data',
    created_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

    CONSTRAINT fk_vlan_device   FOREIGN KEY (device_id) REFERENCES devices(id) ON DELETE CASCADE,
    CONSTRAINT chk_vlan_tag     CHECK (vlan_tag BETWEEN 1 AND 4096),
    CONSTRAINT chk_vlan_status  CHECK (status IN ('Active', 'Suspended')),
    CONSTRAINT chk_vlan_type    CHECK (type IN ('Ethernet', 'FDDI', 'TokenRing', 'FDDI-Net', 'TrBRF', 'TrCRF')),
    CONSTRAINT chk_vlan_role    CHECK (role IN ('Data', 'Voice', 'Management', 'Guest', 'Blackhole')),
    CONSTRAINT uq_vlan_device_tag UNIQUE (device_id, vlan_tag)
);

