    CONSTRAINT fk_psec_interface FOREIGN KEY (interface_id) REFERENCES interfaces(id) ON DELETE CASCADE,
    CONSTRAINT chk_psec_mode     CHECK (violation_mode IN ('Shutdown', 'Restrict', 'Protect'))
