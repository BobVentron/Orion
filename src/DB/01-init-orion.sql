CREATE TABLE lag_members (
    id                    INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    agg_interface_id      BIGINT NOT NULL,
    member_interface_id   BIGINT NOT NULL,
    protocol              VARCHAR(20) DEFAULT 'LACP',
    actor_oper_key        INT,
    partner_oper_system   VARCHAR(100),
    state_flags           VARCHAR(20),
    is_synced             BOOLEAN DEFAULT FALSE,
    is_standby            BOOLEAN DEFAULT FALSE,

    CONSTRAINT fk_lag_agg    FOREIGN KEY (agg_interface_id)    REFERENCES interfaces(id) ON DELETE CASCADE,
    CONSTRAINT fk_lag_member FOREIGN KEY (member_interface_id) REFERENCES interfaces(id) ON DELETE CASCADE,
    CONSTRAINT chk_lag_proto CHECK (protocol IN ('LACP', 'PAgP', 'Static')),

    -- Évite les doublons membre↔agrégat
    CONSTRAINT uq_lag_member UNIQUE (agg_interface_id, member_interface_id)
