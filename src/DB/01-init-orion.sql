    PRIMARY KEY (interface_id, last_change),

    CONSTRAINT fk_status_interface FOREIGN KEY (interface_id) REFERENCES interfaces(id) ON DELETE CASCADE,
    CONSTRAINT chk_if_admin_status CHECK (admin_status IN ('Up', 'Down', 'Testing')),
    CONSTRAINT chk_if_oper_status  CHECK (oper_status  IN ('Up', 'Down', 'Testing', 'Unknown'))
