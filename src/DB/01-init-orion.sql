DROP TABLE IF EXISTS auth_profiles CASCADE;
DROP TABLE IF EXISTS auth_cli CASCADE;
DROP TABLE IF EXISTS auth_snmp CASCADE;

CREATE TABLE auth_snmp (
    id              INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(100) NOT NULL,
    version         VARCHAR(10)  NOT NULL DEFAULT 'v2c',
    port            INTEGER      NOT NULL DEFAULT 161,
    community       VARCHAR(255),
    v3_user         VARCHAR(100),
    v3_level        VARCHAR(20),
    v3_auth_proto   VARCHAR(20),
    v3_auth_pass    VARCHAR(255),
    v3_priv_proto   VARCHAR(20),
    v3_priv_pass    VARCHAR(255),
    created_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

    CONSTRAINT chk_snmp_version    CHECK (version IN ('v1', 'v2c', 'v3')),
    CONSTRAINT chk_snmp_port       CHECK (port BETWEEN 1 AND 65535),
    CONSTRAINT chk_snmp_level      CHECK (v3_level IN ('noAuthNoPriv', 'authNoPriv', 'authPriv')),
    CONSTRAINT chk_snmp_auth_proto CHECK (v3_auth_proto IN ('MD5', 'SHA', 'SHA224', 'SHA256', 'SHA384', 'SHA512')),
    CONSTRAINT chk_snmp_priv_proto CHECK (v3_priv_proto IN ('DES', '3DES', 'AES', 'AES128', 'AES192', 'AES256'))
);

CREATE TABLE auth_cli (
    id              INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(100) NOT NULL,
    username        VARCHAR(100) NOT NULL,
    password        VARCHAR(255),
    enable_password VARCHAR(255),
    ssh_key_path    VARCHAR(255),
    protocol_pref   VARCHAR(10)  NOT NULL DEFAULT 'SSH',
    port            INTEGER      NOT NULL DEFAULT 22,
    created_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

    CONSTRAINT chk_cli_protocol CHECK (protocol_pref IN ('SSH', 'Telnet')),
    CONSTRAINT chk_cli_port     CHECK (port BETWEEN 1 AND 65535)
);

CREATE TABLE auth_profiles (
    id              INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            VARCHAR(100) NOT NULL,
    snmp_id         INT,
    cli_id          INT,
    created_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at      TIMESTAMP WITH TIME ZONE DEFAULT NOW(),

    CONSTRAINT fk_profile_snmp FOREIGN KEY (snmp_id) REFERENCES auth_snmp(id) ON DELETE SET NULL,
    CONSTRAINT fk_profile_cli  FOREIGN KEY (cli_id)  REFERENCES auth_cli(id)  ON DELETE SET NULL
);

CREATE INDEX idx_auth_profiles_snmp ON auth_profiles(snmp_id);
CREATE INDEX idx_auth_profiles_cli  ON auth_profiles(cli_id);


